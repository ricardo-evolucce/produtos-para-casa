  <script>
    const searchInput = document.getElementById("searchInput");
    const categoryFilter = document.getElementById("categoryFilter");
    const priceFilter = document.getElementById("priceFilter");
    const productCards = document.querySelectorAll(".product-card");

    function filterProducts() {
      const search = searchInput.value.toLowerCase();
      const category = categoryFilter.value;
      const priceRange = priceFilter.value;

      productCards.forEach(card => {
        const name = card.querySelector("h4").textContent.toLowerCase();
        const categoryData = card.dataset.category;
        const priceData = parseFloat(card.dataset.price);

        const matchesSearch = name.includes(search);
        const matchesCategory = category === "" || category === categoryData;
        const matchesPrice =
          priceRange === "" ||
          (priceRange === "0-100" && priceData <= 100) ||
          (priceRange === "101-300" && priceData > 101 && priceData <= 300) ||
          (priceRange === "301-5000" && priceData > 301);

        card.style.display = matchesSearch && matchesCategory && matchesPrice
          ? "block"
          : "none";
      });
    }

    searchInput.addEventListener("input", filterProducts);
    categoryFilter.addEventListener("change", filterProducts);
    priceFilter.addEventListener("change", filterProducts);
